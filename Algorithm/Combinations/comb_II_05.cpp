#include <vector>
#include <iostream>
using namespace std;

int main(){
    int* arr = new int[3]{4,3,5};

    int max_comb = 1;
    int temp;
    int index;

    for(int i=0; i<3; ++i){
        max_comb *= arr[i];
    }
//    cout<<max_comb;
    for(int i=0; i<max_comb; ++i){
        temp = i;
        cout<<i<<":\t";
        for(int j=0; j<3; ++j){
            index = temp % arr[j];
            temp /= arr[j];
            cout <<"j:"<<j<<" "<<index <<", ";
        }
        cout<<endl;
    }
}



